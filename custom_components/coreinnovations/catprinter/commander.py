"""Low-level command framing for cat-printer (M02 / MX family) BLE printers.

The Core Innovations / DP Audio Video CTP500 is a BLE thermal printer in the
"cat printer" family.  It is NOT an ESC/POS device: there is no on-board text,
QR or barcode engine.  Everything is rasterised to a 384px wide (48 byte) 1bpp
bitmap on the host and streamed to the printer one row at a time.

Every command frame looks like::

    0x51 0x78 <cmd> 0x00 <len_lo> <len_hi> <payload...> <crc8(payload)> 0xff

``len`` is the payload length as a little-endian uint16 (the high byte is almost
always 0x00 because payloads are tiny).  The CRC8 covers the payload bytes only.

This module is a faithful port of the framing used by the open-source
Cat-Printer project (NaitLee/Cat-Printer); it intentionally contains no I/O so
it can be unit-tested in isolation.
"""

