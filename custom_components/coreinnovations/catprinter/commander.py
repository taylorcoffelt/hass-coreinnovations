def crc8(data: bytes) -> int:
    """Return the CRC8 (poly 0x07) of ``data``."""
    crc = 0
    for byte in data:
        crc = CRC8_TABLE[(crc ^ byte) & 0xFF]
    return crc & 0xFF


def reverse_bits(value: int) -> int:
    """Reverse the bit order of a single byte (printer reads LSB-first)."""
    value = ((value & 0b10101010) >> 1) | ((value & 0b01010101) << 1)
    value = ((value & 0b11001100) >> 2) | ((value & 0b00110011) << 2)
    value = ((value & 0b11110000) >> 4) | ((value & 0b00001111) << 4)
    return value & 0xFF


class Command:
    """Command opcodes used by the AE00 cat-printer variant (e.g. CTP500)."""

    GET_DEVICE_STATE = 0xA3
    GET_DEVICE_INFO = 0xA8
    UPDATE_DEVICE = 0xA9    # commit settings before a print run
    SET_DPI = 0xA4          # payload 0x32 (50) selects 200 DPI
    SET_SPEED = 0xBD        # uint8, lower = faster (values < 4 may stall feeding)
    SET_ENERGY = 0xAF       # uint16 LE, darkness (default ~0x3000, max 0xffff)
    APPLY_ENERGY = 0xBE     # payload 0x01
    LATTICE = 0xA6          # start/end "lattice" (drawing mode)
    DRAW_BITMAP = 0xA2      # one 48-byte row, each byte bit-reversed
    FEED_PAPER = 0xA1       # uint16 LE pixels
    RETRACT_PAPER = 0xA0    # uint16 LE pixels


# 11-byte "magic" payloads that bracket a draw session.
_START_LATTICE = bytes([0xAA, 0x55, 0x17, 0x38, 0x44, 0x5F, 0x5F, 0x5F, 0x44, 0x38, 0x2C])
_END_LATTICE = bytes([0xAA, 0x55, 0x17, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x17])


def make_command(command: int, payload: bytes) -> bytes:
    """Wrap ``payload`` for ``command`` in the cat-printer frame."""
    length = len(payload)
    return bytes(
        [0x51, 0x78, command & 0xFF, 0x00, length & 0xFF, (length >> 8) & 0xFF]
        + list(payload)
        + [crc8(payload), 0xFF]
    )


def get_device_state() -> bytes:
    return make_command(Command.GET_DEVICE_STATE, b"\x00")


def get_device_info() -> bytes:
    return make_command(Command.GET_DEVICE_INFO, b"\x00")


def start_printing() -> bytes:
    """Raw "begin print run" frame (identical bytes to get_device_state)."""
    return bytes([0x51, 0x78, 0xA3, 0x00, 0x01, 0x00, 0x00, 0x00, 0xFF])


def update_device() -> bytes:
    """Commit pending settings just before drawing (Cat-Printer 0xA9)."""
    return make_command(Command.UPDATE_DEVICE, b"\x00")


def set_dpi_as_200() -> bytes:
    return make_command(Command.SET_DPI, bytes([50]))


def set_speed(speed: int) -> bytes:
    return make_command(Command.SET_SPEED, bytes([speed & 0xFF]))


def set_energy(energy: int) -> bytes:
    energy &= 0xFFFF
    return make_command(Command.SET_ENERGY, bytes([energy & 0xFF, (energy >> 8) & 0xFF]))


def apply_energy() -> bytes:
    return make_command(Command.APPLY_ENERGY, b"\x01")


def start_lattice() -> bytes:
    return make_command(Command.LATTICE, _START_LATTICE)


def end_lattice() -> bytes:
    return make_command(Command.LATTICE, _END_LATTICE)


def draw_bitmap(row: bytes) -> bytes:
    """Encode one bitmap row; each byte is bit-reversed before framing."""
    return make_command(Command.DRAW_BITMAP, bytes(reverse_bits(b) for b in row))


def feed_paper(pixels: int) -> bytes:
    pixels &= 0xFFFF
    return make_command(Command.FEED_PAPER, bytes([pixels & 0xFF, (pixels >> 8) & 0xFF]))


def retract_paper(pixels: int) -> bytes:
    pixels &= 0xFFFF
    return make_command(Command.RETRACT_PAPER, bytes([pixels & 0xFF, (pixels >> 8) & 0xFF]))
